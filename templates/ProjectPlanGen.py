def para(doc, txt, indent=0.74, fs=12):
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.0
    p.paragraph_format.widow_control = True
    p.paragraph_format.keep_together = True
    p.paragraph_format.first_line_indent = Cm(indent)
    p.paragraph_format.left_indent = Cm(0)
    
    # Auto-bold key terms
    bold_kw = ['智鉴药靶','TCM-TargetMiner','六步全流程','全流程闭环','全流程无缝整合',
        '3分钟','2至3天','独家收录','苗药','全球首个','全球范围内',
        '论文级3D可视化','论文发表级','12800元','2980元','6980元','20000元',
        '开源技术栈','零边际成本','自动完成','无需人工干预']
    remaining = txt
    while remaining:
        pos = len(remaining)
        kw = None
        for k in bold_kw:
            p2 = remaining.find(k)
            if p2 != -1 and p2 < pos:
                pos = p2; kw = k
        if kw is None:
            r = p.add_run(remaining)
            r.font.size = Pt(fs); r.font.name = '宋体'
            r.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
            break
        if pos > 0:
            r = p.add_run(remaining[:pos])
            r.font.size = Pt(fs); r.font.name = '宋体'
            r.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
        r = p.add_run(kw)
        r.font.size = Pt(fs); r.font.name = '宋体'
        r.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
        r.font.bold = True
        remaining = remaining[pos + len(kw):]
    return p

def bpara(doc, txt, indent=0.74, fs=12):
    """支持 **加粗** 标记的段落"""
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.0
    p.paragraph_format.widow_control = True
    p.paragraph_format.keep_together = True
    p.paragraph_format.first_line_indent = Cm(indent)
    p.paragraph_format.left_indent = Cm(0)
    parts = txt.split('**')
    for i, seg in enumerate(parts):
        if not seg:
            continue
        r = p.add_run(seg)
        r.font.size = Pt(fs); r.font.name = '宋体'
        r.element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
        if i % 2 == 1:  # odd indices are between ** markers
            r.font.bold = True
    return p

def hd(doc, txt, lv=1):
    h = doc.add_heading(txt, level=lv)
    for r in h.runs: r.font.color.rgb = C_DRED
    pf = h.paragraph_format
    pf.first_line_indent = Cm(0)
    pf.line_spacing = 1.5
    # Apply numbering to level 1 headings
    if lv == 1 and hasattr(doc, '_numId') and doc._numId:
        from docx.oxml import OxmlElement
        from docx.oxml.ns import qn as qn2
        pPr = h._p.get_or_add_pPr()
        numPr = OxmlElement('w:numPr')
        ilvl = OxmlElement('w:ilvl'); ilvl.set(qn2('w:val'), '0')
        numId = OxmlElement('w:numId'); numId.set(qn2('w:val'), doc._numId)
        numPr.append(ilvl); numPr.append(numId)
        pPr.append(numPr)
    return h

def tbl(doc, hds, rows):
    t = doc.add_table(rows=1+len(rows), cols=len(hds))
    t.style = 'Table Grid'; t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for j, h in enumerate(hds):
        c = t.rows[0].cells[j]; c.text = h
        for p in c.paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for r in p.runs: r.font.bold = True; r.font.size = Pt(11)
    for i, row in enumerate(rows):
        for j, v in enumerate(row):
            c = t.rows[i+1].cells[j]; c.text = str(v)
            for p in c.paragraphs:
                for r in p.runs: r.font.size = Pt(11)

